        activeIndex: 'template',
        initForm: '',
        isUpdate: false,
        flag: ''
      }
    },
    methods: {
      setEditForm (val, flag) {
        this.initForm = val
        this.flag = flag
      },
      setUpdate (val) {
        this.isUpdate = val
      },
      reback (val) {
        this.isUpdate = val
